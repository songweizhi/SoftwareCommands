
# TreeGraph2

# 1. click on the root of the tree to be exported
# 2. File -> Export to tree format
# 3. In "File Format", select "Newick file"
# 4. In " Node name format", select "Spaces as underscores, no quotation markers"
# 5. In "Branch lengths", select "Do not export branch lengths"
# 6. Open the exported tree file, remove "[&r]" from the beginning; Add "<45" to the end (before symbol ";"); and add the number of leaves (followed with "tab 1") to the first line. 
# 7. now it's ready for mcmctree
